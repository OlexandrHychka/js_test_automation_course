// Напишіть функцію, яка повертає суму A та B.

let a = 15
let b = 23
function sumAB(a, b){
    return a + b;
}
var myResult = sumAB(a, b)
console.log("Сума A та B = " + myResult)

// Напишіть функцію, яка повертає добуток (множення) A та B.

